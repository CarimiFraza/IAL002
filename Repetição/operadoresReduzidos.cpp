#include <iostream>
main()
{
	int x;
	
	x = 1;
	printf (" x vale : %d \n", x);  // x = 1
	x += 3; // x = x+3;
	printf (" x vale : %d \n", x);  // x=4  
	x *= 5; // x = x*5;
	printf (" x vale : %d \n", x);  // x=20  
	x++;  // x = x+1
	printf (" x vale : %d \n", x);  // x=21
	x--;  // x = x+1
	printf (" x vale : %d \n", x);  // x=20	
}
